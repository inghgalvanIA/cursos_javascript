//redondear un numero hacia abajo

console.log(Math.floor(2.3));

//valor de pi
console.log(Math.PI);

//redondear con reglas de redondeo
console.log(Math.round(2.6));

//redondear hacia arriba
console.log(Math.ceil(2.1));

//sacar la raiz cuadrada
console.log(Math.sqrt(33));

//valor absoluto
console.log(Math.abs(-500));

//potencia
console.log(Math.pow(8,3));

//para saber cual es el minimo entre varios numeros
console.log(Math.min(7,3,4,5));

//para saber cual es el maximo de varios numeros
console.log(Math.max(10,12,1,2));

//obtener un numero random
console.log(Math.random);

