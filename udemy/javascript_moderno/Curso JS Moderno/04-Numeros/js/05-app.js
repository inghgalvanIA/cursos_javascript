//decremento o incremento

let puntaje = 10;

//incrementar en 1

puntaje++;
console.log(puntaje)

//incrementar un valor en especifico

puntaje += 3;
console.log(puntaje)

//decrementar en 1
puntaje--;
console.log(puntaje)

//decrementar en un valor en especifico
puntaje -= 2;
console.log(puntaje)
